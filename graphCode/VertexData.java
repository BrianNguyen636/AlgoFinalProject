/**
 * Data class to hold vertex data.
 * 
 * @author Brian Nguyen
 * @version 05/12/2024
 */
public class VertexData {
    int height;
    int excess;

    /**
     * Initializes a VertexData object.
     * @param h the height.
     * @param e the excess.
     */
    public VertexData(int h, int e) {
        height = h;
        excess = e;
    }
}
